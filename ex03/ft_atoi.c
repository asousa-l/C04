int	ft_atoi(char *str)
{
	int	c;
	int	s;
	int	tot;

	i = 0;
	s = 1;
	tot = 0;
	while ((str[i] >= '\t' && str[i] <= '\r') || str[i] == ' ')
		i++;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			s *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		tot = (str[i] - '0') + (tot * 10);
		i++;
	}
	return (tot * s);
}
